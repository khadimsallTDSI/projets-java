package pack1;

public class Adresse{
	int numero;
	String quartier;
	short rue;
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero=numero;
	}
	public String getQuartier() {
		return quartier;
	}
	public void setQuartier(String quartier) {
		this.quartier=quartier;
	}
	public short getRue() {
		return rue;
	}
	public void setRue(short rue) {
		this.rue=rue;
	}
}

